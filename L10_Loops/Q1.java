package L10_Loops;

public class Q1 {
    public static void main(String[] args) {
        // Q1 How many times 'Hello' is printed?
        // Ans. 2 times


        for(int i=0;i<5;i++){
            System.out.println("Hello");
            i+=2;
        }
        
        
        
        
    }
}
