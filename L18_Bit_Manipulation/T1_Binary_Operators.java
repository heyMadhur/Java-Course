package L18_Bit_Manipulation;

public class T1_Binary_Operators {
    
    public static void main(String[] args) {
        // Binary Operators-:

        // 1. Bitwise AND &
        System.out.println("5 & 6= "+(5 & 6));
        
        // 2. Bitwise OR |
        System.out.println("5 | 6= "+(5 | 6));

        // 3. Bitwise XOR ^
        System.out.println("5 ^ 6= "+(5 ^ 6));

        // 4. Binary 1s Complement ~
        System.out.println("~5= "+(~5));
        
        // 5. Binary Left Shift <<
        System.out.println("5<<2= "+(5<<2));
        
        // 6. Binary Right Shift >>
        System.out.println("6>>1= "+(6>>1));
        

        
        
    }
    
}
